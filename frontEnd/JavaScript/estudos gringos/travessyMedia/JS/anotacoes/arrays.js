const fruits = ['apple', 'banana', 'orange'];

console.log(fruits);

fruits.push('mangos');

console.log(fruits); // Adicionei mais um valor na final da array

fruits.unshift('pineapple');

console.log(fruits); // Adicionei mais um valor no inicio da array

fruits.pop(); // Tirar o ultimo valor da array

console.log(fruits);

console.log(fruits.indexOf('orange')); // Dizer em qual posiçao o valor orange está na array